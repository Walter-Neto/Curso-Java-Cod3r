package Fundamentos.operadores;

public class Relacionais {
	
	public static void main(String[] args) {
		System.out.println("1 > 2\n" + (1 > 2));
		
		System.out.println("\n"
				+ "3 < 4\n" + (3 < 4));
		
		System.out.println("\n"
				+ "5 == 9\n" + (5 == 9));
		
		System.out.println("\n"
				+ "15 != 7\n" + (15 != 7));
		
		System.out.println("\n"
				+ "5 >= 4\n" + (3 > 4));
	}

}
