package Fundamentos.operadores;

public class Atribuicao {
	
	public static void main(String[] args) {
		
		int a = 3;
		int b = a;
		int c = a + b;
		
		c += b; // c = c + b;
		c -= a; // c = c - a;
		c *= c; // c = c * b;
		c /= a; // c = c / a;
		c %= 2; // c = c % 2;
		c++; // c = c + 1;
		
	}

}
